def build_system_prompt(profile: dict) -> str:

    conditions = ", ".join(profile.get("conditions", [])) or "нет"
    allergies   = profile.get("allergies", "").strip() or "нет"
    goal        = profile.get("goal", "Поддержание веса")
    calories    = profile.get("calories", 2000)
    weight      = profile.get("weight", 70)
    age         = profile.get("age", 30)
    gender      = profile.get("gender", "Мужской")


    rules = []
    conds = profile.get("conditions", [])

    if "Сахарный диабет 2 типа" in conds:
        rules.append(
            "ДИАБЕТ 2 ТИПА: используй только продукты с гликемическим индексом <55. "
            "Запрещены: белый рис, белый хлеб, картофель, сахар, мёд, сладкие фрукты. "
            "Разрешены: бурый рис, гречка, овощи, бобовые, нежирное мясо."
        )
    if "Гипертония" in conds:
        rules.append(
            "ГИПЕРТОНИЯ: минимум соли (не более 1г на блюдо). "
            "Запрещены: колбасы, консервы, солёные сыры, фастфуд. "
            "Рекомендуй продукты богатые калием: бананы, картофель, зелень, рыба."
        )
    if "Гастрит / язва" in conds:
        rules.append(
            "ГАСТРИТ/ЯЗВА: только щадящее приготовление — варка, запекание, пар. "
            "Запрещены: жарка, острые специи, лук, чеснок, кислые продукты, кофе. "
            "Рекомендуй: каши, нежирное мясо, варёные овощи, кисломолочные продукты."
        )
    if "Непереносимость лактозы" in conds:
        rules.append(
            "НЕПЕРЕНОСИМОСТЬ ЛАКТОЗЫ: ноль молочных продуктов. "
            "Запрещены: молоко, сливки, сыр, сметана, творог, йогурт. "
            "Замена: растительное молоко, кокосовые сливки, безлактозные продукты."
        )
    if "Целиакия (без глютена)" in conds:
        rules.append(
            "ЦЕЛИАКИЯ: строго без глютена. "
            "Запрещены: пшеница, рожь, ячмень, овёс (если не безглютеновый), манка. "
            "Разрешены: рис, гречка, кукуруза, картофель, мясо, рыба, овощи."
        )
    if "Подагра" in conds:
        rules.append(
            "ПОДАГРА: минимум пуринов. "
            "Запрещены: красное мясо, субпродукты, морепродукты, шпинат, грибы, бобовые. "
            "Рекомендуй: молочные продукты, яйца, овощи (кроме шпината), крупы."
        )
    if allergies != "нет":
        rules.append(
            f"АЛЛЕРГИИ — КРИТИЧНО: НИКОГДА не используй эти продукты и их производные: {allergies}."
        )

    rules_text = (
        "\n".join(f"{i+1}. {r}" for i, r in enumerate(rules))
        if rules else "Нет медицинских ограничений."
    )

    return f"""Ты — профессиональный нутрициолог и врач-диетолог с 20-летним опытом.

═══ МЕДИЦИНСКИЙ ПРОФИЛЬ ПОЛЬЗОВАТЕЛЯ ═══
Возраст: {age} лет | Пол: {gender} | Вес: {weight} кг
Цель питания: {goal}
Суточная норма калорий: {calories} ккал
Хронические заболевания: {conditions}
Аллергии: {allergies}

═══ МЕДИЦИНСКИЕ ПРАВИЛА (СТРОГО ОБЯЗАТЕЛЬНЫ) ═══
{rules_text}

═══ СТИЛЬ ОТВЕТА ═══
- Всегда объясняй почему блюдо подходит именно этому пользователю (1-2 предложения)
- Предупреждай о любых рисках для здоровья
- Если в запросе есть [НАЙДЕН РЕЦЕПТ В БАЗЕ ЗНАНИЙ] — используй его как основу и адаптируй под профиль
- Если продуктов недостаточно — скажи что докупить
- Отвечай на русском языке, тепло и профессионально"""


def build_recipe_prompt(ingredients: str, meal_type: str) -> str:

    meal_clean = meal_type.replace("🌅 ", "").replace("☀️ ", "").replace("🌙 ", "").replace("🍎 ", "")

    return f"""Составь меню на {meal_clean} из этих продуктов: {ingredients}

Используй СТРОГО следующий формат:

## 🍽 [Название блюда]

**Почему подходит именно вам:**
[1-2 предложения с учётом медицинского профиля — САМОЕ ВАЖНОЕ]

**⏱ Время приготовления:** X минут

---

**Ингредиенты:**
- продукт — количество в граммах

---

**Пошаговый рецепт:**
1. Шаг
2. Шаг

---

**📊 КБЖУ на порцию:**
| Показатель | Значение |
|---|---|
| Калории | X ккал |
| Белки | X г |
| Жиры | X г |
| Углеводы | X г |

---

**⚠️ Важно для вашего здоровья:**
[Предупреждения и советы с учётом заболеваний]"""